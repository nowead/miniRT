#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;

	mem = malloc(count * size);
	if (mem == NULL)
		return (NULL);
	if (count * size == 0)
		ft_bzero(mem, 1);
	else
		ft_bzero(mem, count * size);
	return (mem);
}
